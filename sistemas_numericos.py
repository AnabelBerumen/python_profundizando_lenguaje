# Decimal
a = 10
print(f'a decimal {a}')
a = 0b1010
print(f'a binario {a}')
a = 0o12
print(f'a octal {a}')
a = 0xA
print(f'a hexadecimal {a}')


# convertir tipo entero, incluyendo la base
a = int('23', 10)
print(f'Decimal conv {a}')

#nase binario
a = int('10111', 2)
print(f'Binario conv {a}')
a = int('27', 8)
print(f'Octal conv {a}')
a = int('17', 16)
